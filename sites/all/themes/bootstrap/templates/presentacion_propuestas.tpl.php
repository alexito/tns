      <h3>
        PROCESO PARA PRESENTACIÓN DE PROPUESTAS ARTÍSTICO - CULTURALES
      </h3>
      <hr>
      <p>
        En su estructura interna, la Fundación Teatro Nacional Sucre cuenta con la Comisión Interna de Programación - CIP- que está conformada por los/as directores/as de la institución: Directora Ejecutiva, Director Musical, Director de Producción, Directora Administrativa, Asesora Legal, Coordinadora de Teatro Variedades y Teatro México, Coordinadora del Centro Cultural Mama Cuchara, Directora de Comunicación y Marketing y Coordinadora Financiera.
      </p>
      <p>
        En esta Comisión, que se reúne semanalmente (máximo quincenalmente), se tratan todos y cada uno de los proyectos y solicitudes que ingresan a la Fundación Teatro Nacional Sucre. Las políticas de análisis consideran varios aspectos como: calidad, tiempo, apoyo, co-producción, y otros.
      </p>
      <p>
        Una vez que se realiza este análisis, la Comisión da respuesta a los solicitantes, a través de comunicaciones formales.
      </p>
      <hr>
      <h3>
        ¿QUÉ DEBO HACER PARA ENVIAR UNA PROPUESTA?
      </h3>
      <hr>
      <p>
        Dirigir un oficio a la Maestra Chía Patiño, Directora Artística y Ejecutiva de la Fundación Teatro Nacional Sucre, en la cual se especifique, lo más detallado posible, el proyecto en mención. A este oficio debe adjuntarse video o fotografías de la obra, de no tenerlas especificar esto en el oficio. Así mismo, debe indicarse el escenario que se solicita, el día previsto para la presentación, así como el tipo de apoyo que se solicita de la Fundación.
      </p>
      <p>
        El oficio debe enviarse a las oficinas de la Fundación Teatro Nacional Sucre, ubicadas en Quito, en la calle Manabí N8-131 entre Guayaquil y Flores o al mail de Analía Borja (Asistente de la Dirección Ejecutiva) aborja@teatrosucre.com
      </p>
      <p>
        Mayor información: (02) 2951661, ext. 111
      </p>
